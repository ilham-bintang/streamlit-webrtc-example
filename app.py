                if pred['scores'] > self.confidence_threshold:
                    # extract the index of the class label from the `detections`,
                    # then compute the (x, y)-coordinates of the bounding box for
                    # the object
                    idx = pred['labels']
                    box = pred['boxes']
                    (startX, startY, endX, endY) = box.astype("int")

                    name = COCO_INSTANCE_CATEGORY_NAMES[idx]
                    result.append(Detection(name=name, prob=float(pred['scores'])))

